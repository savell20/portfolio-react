function generateMd(moodKey, mood) {
  const [bg, surface, accent, muted, ink] = mood.palette
  return `# Design System, ${mood.label} mood

> ${mood.blurb}

## Palette

| Role     | Hex       |
|----------|-----------|
| Background | \`${bg}\` |
| Surface    | \`${surface}\` |
| Accent     | \`${accent}\` |
| Muted      | \`${muted}\` |
| Ink        | \`${ink}\` |

## Typography

- **Display:** ${mood.typography.display}
- **Body:** ${mood.typography.body}
- **Mono:** ${mood.typography.mono}

## Spacing

${mood.spacing}

## Radii

${mood.radii}

## Shadows

${mood.shadows}

## Voice

${mood.voice}

## Usage notes

- Treat **Accent** as the single hero color. Resist adding more.
- **Ink** is for primary text; **Muted** for secondary; never put Ink on Background without a Surface card between them.
- Pair display type sparingly, one headline per viewport.
- This file was generated by the *${mood.label}* mood (\`${moodKey}\`). Paste it into Claude Code, Cursor, or Codex as the design-system context for a new project.
`
}

