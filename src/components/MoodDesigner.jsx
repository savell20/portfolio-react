/* ─────────────────────────────────────────────────────────────
   MODAL
   ───────────────────────────────────────────────────────────── */
export function MoodDesignerModal({ onClose }) {
  const [moodKey, setMoodKey] = useState(null)
  const [palette, setPalette] = useState(null)
  const [copied, setCopied] = useState(false)

  const pickMood = (key) => {
    setMoodKey(key)
    setPalette([...MOODS[key].palette])
  }

  const mood = moodKey ? { ...MOODS[moodKey], palette } : null
  const md = mood ? generateMd(moodKey, mood) : ''

  const copyMd = async () => {
    try {
      await navigator.clipboard.writeText(md)
      setCopied(true)
      setTimeout(() => setCopied(false), 1600)
    } catch { /* */ }
  }
  const downloadMd = () => {
    const blob = new Blob([md], { type: 'text/markdown' })
    const url = URL.createObjectURL(blob)
    const a = document.createElement('a')
    a.href = url
    a.download = `design-system-${moodKey}.md`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
  }
  const resetPalette = () => setPalette([...MOODS[moodKey].palette])

  return createPortal(
    <div
      onPointerDown={(e) => e.stopPropagation()}
      style={{
        position: 'fixed', inset: 0, zIndex: 99995,
        background: 'radial-gradient(ellipse at 50% 35%, #1a1027 0%, #050008 80%)',
        animation: 'fade-in 0.3s ease both',
        display: 'flex', alignItems: 'center', justifyContent: 'center',
        padding: 24,
      }}
    >
      {/* Close */}
      <button
        onClick={onClose}
        aria-label="Close mood designer"
        style={{
          position: 'fixed', top: 18, right: 22, zIndex: 100000,
          display: 'flex', alignItems: 'center', gap: 6,
          padding: '0.55rem 0.9rem', cursor: 'none',
          background: 'rgba(0,0,0,0.45)', border: '1px solid rgba(255,255,255,0.18)',
          borderRadius: 999, color: '#fff',
          fontFamily: 'var(--font-mono)', fontSize: '0.66rem',
        }}
      >
        <X size={13} /> close
      </button>

      {!moodKey && (
        <MoodPicker onPick={pickMood} />
      )}

      {moodKey && (
        <MoodResult
          moodKey={moodKey}
          mood={mood}
          md={md}
          palette={palette}
          onColorChange={(i, v) => setPalette(p => { const n = [...p]; n[i] = v; return n })}
          onReset={resetPalette}
          onBack={() => { setMoodKey(null); setPalette(null) }}
          onCopy={copyMd}
          copied={copied}
          onDownload={downloadMd}
        />
      )}
    </div>,
    document.body
  )
}

